<?php

namespace AppBundle\Helpers;

use Doctrine\ORM\EntityManager;

class StringHelper
{
    static public function currency($money)
    {
        if ($money > 0) {
            return number_format($money, 0, ' ', '.') . ' VND';
        }

        return null;
    }

    static public function slugify($str, $options = array()) {
        $str = trim(mb_strtolower($str));
        $str = preg_replace('/(à|á|ạ|ả|ã|â|ầ|ấ|ậ|ẩ|ẫ|ă|ằ|ắ|ặ|ẳ|ẵ)/', 'a', $str);
        $str = preg_replace('/(è|é|ẹ|ẻ|ẽ|ê|ề|ế|ệ|ể|ễ)/', 'e', $str);
        $str = preg_replace('/(ì|í|ị|ỉ|ĩ)/', 'i', $str);
        $str = preg_replace('/(ò|ó|ọ|ỏ|õ|ô|ồ|ố|ộ|ổ|ỗ|ơ|ờ|ớ|ợ|ở|ỡ)/', 'o', $str);
        $str = preg_replace('/(ù|ú|ụ|ủ|ũ|ư|ừ|ứ|ự|ử|ữ)/', 'u', $str);
        $str = preg_replace('/(ỳ|ý|ỵ|ỷ|ỹ)/', 'y', $str);
        $str = preg_replace('/(đ)/', 'd', $str);
        $str = preg_replace('/[^a-z0-9-\s]/', '', $str);
        $str = preg_replace('/([\s]+)/', '-', $str);
        return $str;
    }

    static public function shortString($text, $limit = 50)
    {
        $result = $text;

        if (strlen($text) > $limit)
        {
            $text   = wordwrap($text, $limit);
            $result = substr($text, 0, strpos($text, "\n"));
        }

        return $result;
    }
}